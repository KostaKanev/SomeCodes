using System;
public class DeclareVariables
{
    public static void Main()
    {
        ushort uShort = 52130;
        sbyte sByte = -115;
        uint uInt = 4825932;
        byte varByte = 97;
        int varInt = -10000;

        Console.WriteLine("{0} - ushort", uShort);
        Console.WriteLine("{0} - sbyte", sByte);
        Console.WriteLine("{0} - uint", uInt);
        Console.WriteLine("{0} - byte", varByte);
        Console.WriteLine("{0} - int", varInt);
    }
}
