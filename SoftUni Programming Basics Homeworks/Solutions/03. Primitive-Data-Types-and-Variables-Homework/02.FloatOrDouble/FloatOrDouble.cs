using System;
public class FloatOrDouble
{
    public static void Main()
    {
        float firstFloat = 12.345f;
        double firstDouble = 34.567839023;
        float secFloat = 3456.091f;
        double secDouble= 8923.1234857;

        Console.WriteLine("{0} - float", firstFloat);
        Console.WriteLine("{0} - double", firstDouble);
        Console.WriteLine("{0} - float", secFloat);
        Console.WriteLine("{0} - double", secDouble);
    }
}
